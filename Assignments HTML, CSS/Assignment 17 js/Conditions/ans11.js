function largest_number(n1, n2){
    if(n1 > n2){
        console.log(`Num1:- ${n1} is Greater Than Num2:- ${n2}`);
    }
    else{
        console.log(`Num2:- ${n2} is Greater Than Num1:- ${n1}`);
    }
}

largest_number(11, 10);
largest_number(9 ,12);

/*
	Num1:- 11 is Greater Than Num2:- 10
    Num2:- 12 is Greater Than Num1:- 9
*/
